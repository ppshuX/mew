function animateLike(button) {
    const icon = button.querySelector('.uz-post-action-icon');
    const originalText = icon.textContent;

    // ÂàõÂª∫ÂøÉÂΩ¢Âä®Áîª
    icon.textContent = '‚ù§Ô∏è';
    icon.style.transform = 'scale(1.5)';
    icon.style.transition = 'transform 0.3s ease';

    // Ê∑ªÂä†Á≤íÂ≠êÊïàÊûú
    createHeartParticles(button);

    setTimeout(() => {
        icon.style.transform = 'scale(1)';
        setTimeout(() => {
            icon.textContent = originalText;
        }, 150);
    }, 300);
}

// ÂàõÂª∫ÂøÉÂΩ¢Á≤íÂ≠êÊïàÊûú
function createHeartParticles(container) {
    for (let i = 0; i < 6; i++) {
        const particle = document.createElement('span');
        particle.textContent = '‚ù§Ô∏è';
        particle.style.cssText = `
            position: absolute;
            font-size: 12px;
            pointer-events: none;
            animation: floatUp 1s ease-out forwards;
            z-index: 1000;
        `;

        const rect = container.getBoundingClientRect();
        particle.style.left = rect.left + Math.random() * rect.width + 'px';
        particle.style.top = rect.top + Math.random() * rect.height + 'px';

        document.body.appendChild(particle);

        setTimeout(() => {
            document.body.removeChild(particle);
        }, 1000);
    }
}

// Êï∞Â≠óÂä®Áîª
function animateNumbers() {
    const numbers = document.querySelectorAll('.uz-stat-number');

    numbers.forEach(number => {
        const finalValue = parseInt(number.textContent);
        const duration = 2000;
        const startTime = Date.now();

        function updateNumber() {
            const elapsed = Date.now() - startTime;
            const progress = Math.min(elapsed / duration, 1);
            const currentValue = Math.floor(finalValue * progress);

            number.textContent = currentValue;

            if (progress < 1) {
                requestAnimationFrame(updateNumber);
            }
        }

        updateNumber();
    });
}

